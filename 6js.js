function circum(r) {
    var myvalue = 2 * 3.14 * r
    return myvalue
}

var result = circum(3)
console.log(result)


function diameter(r) {
    var myvalue = 2 * r
    return myvalue
}

var result = diameter(3)
console.log(result)


